Reset_Handler
	LDR R0, =N
	LDR R1, =ASCII
	LDR R2, [R0]
	AND R3, R2, #0x0F
	CMP R3, #0x9
	ADDHI R3, #0x37
	ADDLS R3, #0x30
	STR R3, [R1], #1
	LSR R3, R2, #4
	CMP R3, #0x9
	ADDHI R3, #0x37
	ADDLS R3, #0x30
	STR R3, [R1]
STOP B STOP
N DCD 0x5D
